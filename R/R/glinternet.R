#' Predict values for a penalized regression model.
#' 
#' @param results The results of \code{\link{fit_model}}.
#' @param newx A data.frame, the new data to use for predictions.
#' @return A vector, the predicted values for a penalized regression model using the new data.
#' @export
predict_model.easy_glinternet <- function(object, newx = NULL) {
  newx <- as.matrix(newx)
  model <- object[["model"]]
  stats::predict(model, X = newx)
}

#' Easily build and evaluate a penalized regression model with interactions.
#'
