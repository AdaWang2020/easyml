#' Fit penalized regression model.
#' 
#' @param object TO BE EDITED.
#' @return A list, the model and the cross validated model.
#' @export
fit_model.easy_glinternet <- function(object) {
  warning("Warning: This is for demo purposes; fit_model for glinternet needs a tweak or two to adhere to best practices.")
  # set model arguments
  model_args <- object[["model_args"]]
  
  # process model_args
  X <- as.matrix(object[["X"]])
  model_args[["X"]] <- X
  model_args[["Y"]] <- object[["y"]]
  model_args[["family"]] <- object[["family"]]
  model_args[["numLevels"]] <- rep(1, ncol(X))

  # build model
  model <- do.call(glinternet::glinternet, model_args)
  object[["model"]] <- model

  # write output
  object
}

